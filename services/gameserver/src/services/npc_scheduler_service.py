"""Compatibility shim post-split (WO-QUALITY-techdebt-scheduler-split).

The NPC scheduler used to be one 7,226-line module. It is now the package
at ``src/services/scheduler/`` (see that package's ``__init__.py`` for the
submodule map and the original design docstring). This module is a thin
re-export shim so every pre-existing import of
``src.services.npc_scheduler_service`` — production code and tests alike —
keeps resolving unchanged. New code should import directly from
``src.services.scheduler.<submodule>`` instead of this shim.

``apply_faction_rep_delta`` is re-imported directly (not re-exported from a
scheduler submodule) to mirror exactly how the pre-split file imported it —
``reputation_team_sweeps.apply_sustained_reputation_drip`` uses its OWN
copy of this same import, so patching it via this shim module does not
affect that call site (see the split's Concerns for the monkeypatch-target
fallout this and similar re-exports carry).
"""

