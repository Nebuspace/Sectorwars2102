def test_sector_destination_pools_deterministic_regardless_of_row_order():
    """Determinism rests ENTIRELY on the internal str(uuid) sorts inside
    celestial_service._merge_real_planets/_make_stations -- there is no
    ORDER BY backstop anywhere in this path (Mack's gate finding). Pin it:
    identical planets/stations, different DB row order -> identical pools
    output, so removing either internal sort fails this test loudly instead
    of jittering NPC destinations silently in prod."""
    sector_id = 48
    celestial_row = _Row(composition={"bodies": [], "stations": []})
    planet_a = _fake_planet(uuid.UUID(int=9001), habitability_score=80, kind="TERRAN")
    planet_b = _fake_planet(uuid.UUID(int=9002), habitability_score=5, kind="BARREN")
    station_a = _fake_station(uuid.UUID(int=9101), name="Dock A")
    station_b = _fake_station(uuid.UUID(int=9102), name="Dock B")

    def run(planet_order, station_order):
        session = _FakeSession(
            sector=_fake_sector(sector_id),
            planets=planet_order,
            stations=station_order,
            celestial_row=celestial_row,
        )
        return sector_destination_pools(session, sector_id, "npc-shuffle")

    forward = run([planet_a, planet_b], [station_a, station_b])
    shuffled = run([planet_b, planet_a], [station_b, station_a])

    def normalize(pools):
        return {
            bucket: sorted(
                (round(e["x_pct"], 9), round(e["y_pct"], 9), e["target_kind"], e["target_id"])
                for e in entries
            )
            for bucket, entries in pools.items()
        }

    forward_n, shuffled_n = normalize(forward), normalize(shuffled)
    assert forward_n == shuffled_n
    assert any(forward_n.values())  # sanity: not accidentally empty
