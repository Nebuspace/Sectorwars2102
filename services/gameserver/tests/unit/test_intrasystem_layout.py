    expected_x, expected_y = layout.planet_position(0.4, 30)
    assert abs(dest["x_pct"] - expected_x) < 1e-9
    assert abs(dest["y_pct"] - expected_y) < 1e-9

    idle = empty_idle_pose(sector_id, "npc-trader-1")
