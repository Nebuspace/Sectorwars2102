def test_orbital_position_nan_input_yields_finite_in_band_output():
    """A malformed orbit_au/phase_deg (e.g. corrupt JSONB) must never
    propagate NaN through the safe-radii box -- Python's min/max are
    NaN-order-dependent (max(nan, x) is nan, but max(x, nan) is x), so this
    pins the explicit guard rather than trusting caller-side filtering."""
    star = star_anchor(1)
    radii = _radii(star, PLANET_FOOTPRINT_EM_MAX)

    for orbit_au, phase_deg in (
        (float("nan"), 45.0),
        (0.5, float("nan")),
        (float("nan"), float("nan")),
    ):
        x, y = orbital_position(star, orbit_au, phase_deg, radii)
        assert not math.isnan(x) and not math.isnan(y)
        assert 0.0 <= x <= 100.0
        assert 0.0 <= y <= 100.0


