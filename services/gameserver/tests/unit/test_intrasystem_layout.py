def test_sector_destination_pools_positions_bodies_via_layout():
    """A sector whose persisted skeleton has real bodies/stations already
    merged (the common steady-state case) -- pools land exactly where
    SectorLayout places them, and every position is a real %-space point."""
    sector_id = 42
    composition = {
        "bodies": [
            {"orbit_au": 0.3, "phase_deg": 60, "kind": "TERRAN", "real": False, "planet_id": None},
            {"orbit_au": 0.7, "phase_deg": 210, "kind": "BARREN", "real": False, "planet_id": None},
        ],
        "stations": [],
    }
    celestial_row = _Row(composition=composition)
    session = _FakeSession(sector=_fake_sector(sector_id), planets=[], stations=[], celestial_row=celestial_row)

    pools = sector_destination_pools(session, sector_id, "npc-ship-1")

    layout = SectorLayout(sector_id)
    ex0 = layout.planet_position(0.3, 60)
    ex1 = layout.planet_position(0.7, 210)

    all_planets = pools["habitable"] + pools["barren"]
    assert len(all_planets) == 2
    got = {(round(p["x_pct"], 6), round(p["y_pct"], 6)) for p in all_planets}
    expected = {(round(ex0[0], 6), round(ex0[1], 6)), (round(ex1[0], 6), round(ex1[1], 6))}
    assert got == expected
    # TERRAN body buckets habitable, BARREN buckets barren.
    assert len(pools["habitable"]) == 1
    assert len(pools["barren"]) == 1
    assert len(pools["outbound"]) == 3  # always-populated seeded rim scatter


