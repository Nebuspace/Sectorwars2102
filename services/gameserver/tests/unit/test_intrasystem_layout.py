def test_sector_layout_matches_client_on_three_live_sectors():
    """Cross-language parity proof against REAL sector data (sectors 1, 10,
    11) -- not hand-picked synthetic orbit_au/phase_deg. See module docstring
    for provenance."""
    max_err = 0.0
    for sector_id, data in EXPECTED_LIVE.items():
        layout = SectorLayout(sector_id)
        for orbit_au, phase_deg, ex, ey in data["bodies"]:
            x, y = layout.planet_position(orbit_au, phase_deg)
            max_err = max(max_err, abs(x - ex), abs(y - ey))
        for orbit_au, phase_deg, ex, ey in data["stations"]:
            x, y = layout.station_position(orbit_au, phase_deg)
            max_err = max(max_err, abs(x - ex), abs(y - ey))
    assert max_err < TOL_PCT, f"max per-axis delta {max_err} exceeds tolerance {TOL_PCT}"


