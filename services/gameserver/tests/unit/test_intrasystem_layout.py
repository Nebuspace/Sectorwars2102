def test_leg_generation_targets_a_layout_derived_position():
    """End-to-end: a destination picked from sector_destination_pools (whose
    x/y come from SectorLayout) becomes a real burn leg whose to_x/to_y match
    the layout output exactly -- the actual server-side leg-generation path
    WO-TRANCHE-0716 ISP-PARITY exists to fix."""
    sector_id = 45
    composition = {
        "bodies": [{"orbit_au": 0.4, "phase_deg": 30, "kind": "OCEANIC", "real": False, "planet_id": None}],
        "stations": [],
    }
    celestial_row = _Row(composition=composition)
    session = _FakeSession(sector=_fake_sector(sector_id), planets=[], stations=[], celestial_row=celestial_row)

    pools = sector_destination_pools(session, sector_id, "npc-trader-1")
    dest = pools["habitable"][0]

