    assert abs(pose["leg"]["to_x"] - expected_x) < 1e-9
    assert abs(pose["leg"]["to_y"] - expected_y) < 1e-9

    # Sampling mid-flight lands strictly between the burn's endpoints -- a
    # real leg toward the layout-computed target, not a placeholder scatter.
    from datetime import datetime, timedelta, timezone

    from src.services.intrasystem_movement_service import derive_pose

    now = datetime(2026, 7, 16, 12, 0, 0, tzinfo=timezone.utc)
